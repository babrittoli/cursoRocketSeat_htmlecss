/* 
        EXPRESSÃO "new"

    A expressão new, também denominada como left-hand-side expression, 
serve para criar um novo objeto usando as funções construtoras que temos 
por padrão no JavaScript.
*/

let name = new String('BARBARA')
name.surName = "Brito"
let age = new Number(25)
console.log(name.surName, age)


let date = new date ('2022-08-25')
console.log(date.__proto__)

